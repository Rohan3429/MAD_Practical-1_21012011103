fun main(){
    var a:Int=10
    println("integer value is :$a")
    a.toDouble()
    println("$a double value(from integer)")
    var b:String="10"
    println("string value is :$b")
    b.toInt()
    println("integer value from string is $b")
    b.toDouble()
    println("Double value from string is $b")
}